profile = {
    'name':'raj','age':23,'salary':25000.00
}
print('\nit will only print values: ')
for i in profile:
    print(i)

print('\nAfter using profile.value: ')
for i in profile.values():
    print(i)

print('\nFor get pair of key and value use profile.itmes: ')
for i in profile.items():
    print(i)